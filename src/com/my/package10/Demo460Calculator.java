package com.my.package10;

public class Demo460Calculator {
    /**
     *加法
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b) {
        return a + b;
    }

    /**
     * 减法
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b) {
        return a - b;
    }

}
